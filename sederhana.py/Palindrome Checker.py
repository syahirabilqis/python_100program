# Program Palindrome Checker

print("*"*40)
print("Program Palindrome Checker")
print("*"*40)

kata = input("Masukkan kata: ")

if kata  == kata [:: -1]:
    print(kata, "adalah palindrom")
else :
    print(kata, "bukan palindrome")
    
    
